package Reto;

public class Funciones1 {

	
	public static double MillasaKm(double n1) {
		return n1*1.60934;
	}
	
	public static double KmaMilla(double n1 ) {
		return n1*0.62137;
	}
	
	public static double KilogramosAlibras(double n1) {
		return n1*2.20462;
	}
	public static double LibrasaKilogramos(double n1) {
		return n1*0.453592;
	}
}
